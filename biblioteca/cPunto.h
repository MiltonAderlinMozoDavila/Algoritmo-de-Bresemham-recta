
#include <iostream>
#include <conio.h>
#include <string>

using namespace std;
class cPunto
{
        /* *********************** ATRIBUTOS ************************ */
        private:
            float aX;
            float aY;

        /* *********************** METODOS ************************ */
        public:
        /* ==================== CONSTRUCTORES ======================= */
        // ----------------------------------------------------------------
        cPunto()
        {
            aX = 0.0;
            aY = 0.0;
        }

        // ----------------------------------------------------------------
        cPunto( float pX, float pY)
        {
            aX = pX;
            aY = pY;
        }

        /* ==================== MÉTODOS GET Y SET ======================= */
        // ----------------------------------------------------------------
        float GetX()
        {
            return aX;
        }
        // ----------------------------------------------------------------
        void SetX(float pX)
        {
            aX = pX;
        }
        // ----------------------------------------------------------------
        float GetY()
        {
            return aY;
        }
        // ----------------------------------------------------------------
        void SetY(float pY)
        {
            aY = pY;
        }

        /* ==================== MÉTODOS PROCESO ======================= */
        // ----------------------------------------------------------------
        void Leer()
        {
            cout << "INGRESAR DATOS DE PUNTO" << endl;
            cout << "=======================" << endl;
            cout << "Ingrese X: ";
            cin>>aX;
            cout << "Ingrese Y: ";
            cin>>aY;

        }
        // ----------------------------------------------------------------
        void Mostrar()
        {
            cout << "DATOS DE PUNTO" << endl;
            cout << "==============" << endl;
            cout << "X:" << aX << endl;
            cout << "Y:" << aY << endl;

        }
        // ----------------------------------------------------------------
        void Escribir()
        {
            cout << aX << " " << aY << endl;

        }

    
};
